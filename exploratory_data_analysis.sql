--Measure Exploration
-----------------------------------------
--Find the total sales
SELECT SUM(sales_amount) AS total_sales FROM gold.fact_sales;
--Find how many items were sold
SELECT SUM(quantity) AS total_items_sold FROM gold.fact_sales;
--Find the average selling price
SELECT AVG(price) AS average_price FROM gold.fact_sales;
--Find the total number of orders
SELECT COUNT(order_number) AS total_orders FROM gold.fact_sales;
SELECT COUNT(DISTINCT order_number) AS total_orders FROM gold.fact_sales;
--Find the total number of products
SELECT COUNT(product_name) AS total_products FROM gold.dim_product;
SELECT COUNT(DISTINCT product_key) AS total_products FROM gold.dim_product;
--Find the total number of customers
SELECT COUNT(customer_number) AS total_customers FROM gold.dim_customer;
SELECT COUNT(DISTINCT customer_key) AS total_customers FROM gold.dim_customer;
--Find the total number of customers who placed placed an order
SELECT COUNT(DISTINCT customer_key) AS total_customers FROM gold.fact_sales;
--Generate a report that shows all the key metrics of the business
SELECT 'Total Sales' AS measure_name, SUM(sales_amount) AS value FROM gold.fact_sales
UNION ALL
SELECT 'Total Quantity' AS measure_name, SUM(quantity) AS value FROM gold.fact_sales
UNION ALL
SELECT 'Average Price' AS measure_name, AVG(price) AS value FROM gold.fact_sales
UNION ALL
SELECT 'Total Number of Orders' AS measure_name, COUNT(DISTINCT order_number) AS value FROM gold.fact_sales
UNION ALL
SELECT 'Total Number of Products' AS measure_name, COUNT(DISTINCT product_key) AS value FROM gold.dim_product
UNION ALL
SELECT 'Total Number of Customers' AS measure_name, COUNT(DISTINCT customer_key) AS value FROM gold.dim_customer;
