--Magnitude Analysis
-----------------------------------------
--Find total customers by country
SELECT country, COUNT(DISTINCT customer_key) AS total_customers
FROM gold.dim_customer
GROUP BY country
ORDER BY total_customers DESC;
--Find total customers by gender
SELECT gender,COUNT(DISTINCT customer_key) AS total_customer_by_gender
FROM gold.dim_customer
GROUP BY gender
ORDER BY total_customer_by_gender DESC;
--Find total products by category
SELECT category,COUNT(DISTINCT product_key) AS total_products_by_category
FROM gold.dim_product
GROUP BY category
ORDER BY total_products_by_category DESC;
--What is the average cost in each category?
SELECT category,AVG(cost) AS average_cost_by_category
FROM gold.dim_product
GROUP BY category
ORDER BY average_cost_by_category DESC;
--What is the total revenue generated for each category
SELECT
    dp.category,
    SUM(fs.sales_amount) total_revenue_by_category
FROM gold.fact_sales AS fs
LEFT JOIN gold.dim_product AS dp ON fs.product_key = dp.product_key
GROUP BY dp.category
ORDER BY total_revenue_by_category DESC;
--What is the revenue generated by each customer
SELECT
    dc.customer_key,
    dc.first_name,
    dc.last_name,
    SUM(fs.sales_amount) total_revenue_by_customer
FROM gold.fact_sales AS fs
LEFT JOIN gold.dim_customer AS dc ON fs.customer_key = dc.customer_key
GROUP BY dc.customer_key,
         dc.first_name,
         dc.last_name
ORDER BY total_revenue_by_customer DESC;
--What is the distribution of sold items across countries
SELECT
    dc.country,
    count(fs.product_key) number_of_sold_items
FROM gold.fact_sales AS fs
LEFT JOIN gold.dim_customer AS dc ON fs.customer_key = dc.customer_key
GROUP BY dc.country
ORDER BY number_of_sold_items DESC;
