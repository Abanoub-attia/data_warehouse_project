    MAX(order_date) AS last_order_date,
    DATEDIFF(MONTH, MIN(order_date), MAX(order_date)) AS months_of_sales
FROM gold.fact_sales;
--find the youngest and oldest customers
SELECT 
    MIN(birth_date) AS youngest_customer,
    DATEDIFF(YEAR, MIN(birth_date), GETDATE()) AS youngest_customer_age,
    MAX(birth_date) AS oldest_customer,
    DATEDIFF(YEAR, MAX(birth_date), GETDATE()) AS oldest_customer_age
FROM gold.dim_customer;
