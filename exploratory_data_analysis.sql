--Which 5 products generate the lowest reveneu?
SELECT TOP 5
    dp.product_name,
    SUM(fs.sales_amount) total_revenue_by_product
FROM gold.fact_sales AS fs
LEFT JOIN gold.dim_product AS dp ON fs.product_key = dp.product_key
GROUP BY dp.product_name
ORDER BY total_revenue_by_product ASC;
--Find the top 10 customers who have generated the highest revenue
SELECT TOP 5
    dc.customer_key,
    dc.first_name,
    dc.last_name,
    SUM(fs.sales_amount) total_revenue_by_customer
FROM gold.fact_sales AS fs
LEFT JOIN gold.dim_customer AS dc ON fs.customer_key = dc.customer_key
GROUP BY dc.customer_key,
         dc.first_name,
         dc.last_name
ORDER BY total_revenue_by_customer DESC;
--The 3 customers wih the fewest orders placed
SELECT TOP 3
    dc.customer_key,
    dc.first_name,
    dc.last_name,
    COUNT(DISTINCT fs.order_number) total_number_of_orders_by_customer
FROM gold.fact_sales AS fs
LEFT JOIN gold.dim_customer AS dc ON fs.customer_key = dc.customer_key
GROUP BY dc.customer_key,
         dc.first_name,
         dc.last_name
ORDER BY total_number_of_orders_by_customer ASC;